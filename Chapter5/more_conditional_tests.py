# Only true solutions

guest = 'Harry Styles'
print('Is guest == "Harry Styles"?')
print(guest == 'Harry Styles')

print('Is guest != "Taylor Swift"?')
print(guest != 'Taylor Swift')

car = 'Subaru'
print('\nIs car == "subaru"?')
print(car.lower() == 'subaru')

number = 11
print('\nIs number == 11?')
print(number == 11)
print('Is number > 8?')
print(number > 8)
print('Is number < 15?')
print(number < 15)

pi_value = 3.1416
print('\nIs pi_value < 4 and > 2?')
print(pi_value < 4 and pi_value > 2)

celebrities = ['Harry Styles', 'Taylor Swift', 'Freddie Mercury']
print('\n is "Freddie Mercury" in the list?')
print('Freddie Mercury' in celebrities)
